    public Administrador() {
        initComponents();
        
        setIconImage(Toolkit.getDefaultToolkit().getImage(getClass().getResource("/imagenes/Logo Final.png")));
        user = Log_in.user;
        Secion_usuario = 1;
        
        setSize(944, 494);
        setResizable(false);
        
        setLocationRelativeTo(null);
        setTitle("Administrador - Secion de " + user);
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "select nombre_usuario from usuarios where username = '" + user + "' ");
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                nombre_usuario = rs.getString("nombre_usuario");
                etiketa_Bienbenida.setText("Secion de " + nombre_usuario);
            }
        } catch (Exception e) {
            System.err.println("erro en la coneccion en la interfaz administrador");
        }

    }

